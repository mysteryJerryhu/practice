"""
先将行数i和列数j赋值为1
进入循环，判断行数是否大于10
若不是，进入循环，判断列数是否大于行数
  若不是，输出方程式，并且不换行
  列数+1
  判断列数是否大于行数
  若是，跳出列数循环
输出空值
将列数值强制变为1，进行新一轮循环

i=1 j=1
into:
i=1 j=1----(j<2)----p j=2 out
 i=2,j=1
i=2 j=1----(j<3)----p j=2----(j<3)----p j=3 out
 i=3,j=1
i=3 j=1----(j<4)----p j=2----(j<4)----p j=3----(j<4)----p j=4 out
 i=4,j=1
........
"""

i = 1
j = 1
while i < 10:
    while j < i+1:
        print("%d*%d=%d " % (j, i, i*j), end='')
        j += 1
    print()
    i += 1
    j = 1
